namespace ActionsSandbox.Library
{
    public class Class1
    {
        public int Add(int a, int b)
        {
            return a + b;
        }

        public int Subtract(int left, int right)
        {
            return left - right;
        }

        public int Multiply(int a, int b)
        {
            return (a * b);
        }

        public double Divide(int num, int den)
        {
            return num / (double)den;
        }

    }
}